create table if not exists public.aggregated_stats (
  id uuid primary key default gen_random_uuid(),
  athlete_id bigint not null references public.athletes (athlete_id) on delete cascade,
  bucket_granularity text not null check (bucket_granularity in ('all', 'week', 'month', 'year')),
  bucket_date date not null,
  activity_type text not null default 'all',
  activity_count integer not null default 0,
  total_distance_meters numeric not null default 0,
  total_elevation_gain numeric not null default 0,
  total_moving_time_seconds integer not null default 0,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  unique (athlete_id, bucket_granularity, bucket_date, activity_type)
);

create table if not exists public.sync_logs (
  id uuid primary key default gen_random_uuid(),
  athlete_id bigint references public.athletes (athlete_id) on delete set null,
  sync_type text not null,
  status text not null check (status in ('running', 'success', 'error')),
  activities_processed integer,
  error_message text,
  metadata jsonb,
  started_at timestamptz not null default timezone('utc', now()),
  completed_at timestamptz,
  created_at timestamptz not null default timezone('utc', now())
