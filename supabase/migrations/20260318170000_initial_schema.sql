create or replace function public.refresh_aggregated_stats(p_athlete_id bigint)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  delete from public.aggregated_stats where athlete_id = p_athlete_id;

  insert into public.aggregated_stats (
    athlete_id,
    bucket_granularity,
    bucket_date,
    activity_type,
    activity_count,
    total_distance_meters,
    total_elevation_gain,
    total_moving_time_seconds
  )
  with base as (
    select
      athlete_id,
      sport_type,
      start_date::date as activity_date,
      distance_meters,
      total_elevation_gain,
      moving_time_seconds
    from public.activities
    where athlete_id = p_athlete_id
  ),
  rollups as (
    select athlete_id, 'all'::text as bucket_granularity, current_date as bucket_date, 'all'::text as activity_type,
      count(*)::integer as activity_count,
      coalesce(sum(distance_meters), 0)::numeric as total_distance_meters,
      coalesce(sum(total_elevation_gain), 0)::numeric as total_elevation_gain,
      coalesce(sum(moving_time_seconds), 0)::integer as total_moving_time_seconds
    from base
    group by athlete_id

    union all

    select athlete_id, 'all', current_date, sport_type,
      count(*)::integer,
      coalesce(sum(distance_meters), 0)::numeric,
      coalesce(sum(total_elevation_gain), 0)::numeric,
      coalesce(sum(moving_time_seconds), 0)::integer
    from base
    group by athlete_id, sport_type

    union all

    select athlete_id, 'week', date_trunc('week', activity_date)::date, 'all',
      count(*)::integer,
      coalesce(sum(distance_meters), 0)::numeric,
      coalesce(sum(total_elevation_gain), 0)::numeric,
      coalesce(sum(moving_time_seconds), 0)::integer
    from base
    group by athlete_id, date_trunc('week', activity_date)::date

    union all

    select athlete_id, 'month', date_trunc('month', activity_date)::date, 'all',
      count(*)::integer,
      coalesce(sum(distance_meters), 0)::numeric,
      coalesce(sum(total_elevation_gain), 0)::numeric,
      coalesce(sum(moving_time_seconds), 0)::integer
    from base
    group by athlete_id, date_trunc('month', activity_date)::date

    union all

    select athlete_id, 'year', date_trunc('year', activity_date)::date, 'all',
      count(*)::integer,
      coalesce(sum(distance_meters), 0)::numeric,
      coalesce(sum(total_elevation_gain), 0)::numeric,
      coalesce(sum(moving_time_seconds), 0)::integer
    from base
    group by athlete_id, date_trunc('year', activity_date)::date
  )
  select
    athlete_id,
    bucket_granularity,
    bucket_date,
    activity_type,
    activity_count,
    total_distance_meters,
    total_elevation_gain,
    total_moving_time_seconds
  from rollups;
end;
$$;

create or replace view public.public_sync_status
with (security_invoker = true)
as
select distinct on (athlete_id)
  athlete_id,
  status,
  completed_at,
  activities_processed
from public.sync_logs
where athlete_id is not null
order by athlete_id, coalesce(completed_at, started_at) desc;

create or replace view public.dashboard_overview
with (security_invoker = true)
as
with totals as (
  select
    athlete_id,
    count(*)::integer as activity_count,
    count(*) filter (where sport_type = 'Ride')::integer as ride_count,
    count(*) filter (where sport_type = 'Run')::integer as run_count,
    count(*) filter (where sport_type not in ('Ride', 'Run'))::integer as other_count,
    coalesce(sum(distance_meters), 0)::numeric as total_distance_meters,
    coalesce(sum(total_elevation_gain), 0)::numeric as total_elevation_gain,
    coalesce(sum(moving_time_seconds), 0)::integer as total_moving_time_seconds
  from public.activities
  group by athlete_id
)
select
  a.athlete_id,
  trim(concat(coalesce(a.firstname, ''), ' ', coalesce(a.lastname, ''))) as display_name,
  a.username,
  a.city,
  a.state,
  a.country,
  a.profile,
  a.profile_medium,
  t.activity_count,
  t.ride_count,
  t.run_count,
  t.other_count,
  t.total_distance_meters,
  t.total_elevation_gain,
  t.total_moving_time_seconds,
  s.completed_at as last_synced_at,
  s.status as last_sync_status
from public.athletes a
left join totals t on t.athlete_id = a.athlete_id
left join public.public_sync_status s on s.athlete_id = a.athlete_id;

